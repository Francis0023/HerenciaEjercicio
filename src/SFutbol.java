public class SFutbol {
    int id;
    String nombre;
    String lugar;
    String posicion;

    public SFutbol(int id, String nombre, String lugar, String posicion) {
        this.id = id;
        this.nombre = nombre;
        this.lugar = lugar;
        this.posicion = posicion;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getLugar() {
        return lugar;
    }

    public void setLugar(String lugar) {
        this.lugar = lugar;
    }

    public String getPosicion() {
        return posicion;
    }

    public void setPosicion(String posicion) {
        this.posicion = posicion;
    }
}
